package BasicsPrograms;

public class Ternaryoperator {

	public static void main(String[] args) {

		int i=10,j=20,k=30;
		int temp, result;
		temp = i>j?i:j;
		result = k>temp?k:temp;
		System.out.println(result);
		
		int temp1, result1;
		temp1 = i<j?i:j;
		result1 = k<temp1?k:temp1;
		System.out.println(result1);

	}

}

